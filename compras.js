


document.getElementById('supermercadoButton').addEventListener('click', function() {
    console.log("clicou")
    var supermercadosDiv = document.getElementById('supermercados');
    if (supermercadosDiv.style.display === 'none') {
        supermercadosDiv.style.display = 'block';
    } else {
        supermercadosDiv.style.display = 'none';
    }
});
